// Fungsi untuk mendapatkan semua event
export function getAllEvents() {
  return events;
}

// Fungsi untuk mendapatkan event berdasarkan ID
export function getEventById(id) {
  return events.find(event => event.id === parseInt(id));
}

// Fungsi untuk mendapatkan event featured
export function getFeaturedEvents() {
  return events.filter(event => event.isFeatured);
}

// Fungsi untuk mendapatkan event berdasarkan kategori
export function getEventsByCategory(category) {
  return events.filter(event => event.category === category);
}

// Fungsi untuk mendapatkan event minggu ini
export function getThisWeekEvents() {
  const now = new Date();
  const endOfWeek = new Date(now);
  endOfWeek.setDate(now.getDate() + (7 - now.getDay()));
  
  return events.filter(event => {
    const eventDate = new Date(event.date);
    return eventDate >= now && eventDate <= endOfWeek;
  });
}

// Fungsi untuk mendapatkan event minggu depan
export function getNextWeekEvents() {
  const now = new Date();
  const startOfNextWeek = new Date(now);
  startOfNextWeek.setDate(now.getDate() + (7 - now.getDay()) + 1);
  const endOfNextWeek = new Date(startOfNextWeek);
  endOfNextWeek.setDate(startOfNextWeek.getDate() + 6);
  
  return events.filter(event => {
    const eventDate = new Date(event.date);
    return eventDate >= startOfNextWeek && eventDate <= endOfNextWeek;
  });
}

// Fungsi untuk mendapatkan event bulan ini
export function getThisMonthEvents() {
  const now = new Date();
  const startOfMonth = new Date(now.getFullYear(), now.getMonth(), 1);
  const endOfMonth = new Date(now.getFullYear(), now.getMonth() + 1, 0);
  
  return events.filter(event => {
    const eventDate = new Date(event.date);
    return eventDate >= startOfMonth && eventDate <= endOfMonth;
  });
}

// Fungsi untuk mendapatkan event yang akan datang
export function getUpcomingEvents() {
  const now = new Date();
  return events
    .filter(event => new Date(event.date) >= now)
    .sort((a, b) => new Date(a.date) - new Date(b.date));
}

// Fungsi untuk mendapatkan kategori unik
export function getCategories() {
  return [...new Set(events.map(event => event.category))];
}

// Fungsi untuk mendapatkan lokasi unik
export function getLocations() {
  return [...new Set(events.map(event => event.location))];
}

// Fungsi untuk filter event
export function filterEvents({ category, location, priceType }) {
  return events.filter(event => {
    const matchCategory = !category || event.category === category;
    const matchLocation = !location || event.location === location;
    const matchPrice = !priceType || 
      (priceType === 'gratis' && event.price === 0) ||
      (priceType === 'berbayar' && event.price > 0);
    
    return matchCategory && matchLocation && matchPrice;
  });
}

// Fungsi untuk mendapatkan event berdasarkan tanggal
export function getEventsByDate(date) {
  const targetDate = new Date(date);
  targetDate.setHours(0, 0, 0, 0);
  
  return events.filter(event => {
    const eventDate = new Date(event.date);
    eventDate.setHours(0, 0, 0, 0);
    return eventDate.getTime() === targetDate.getTime();
  });
}

// Fungsi untuk format tanggal ke bahasa Indonesia
export function formatDate(dateString) {
  const options = { 
    weekday: 'long', 
    year: 'numeric', 
    month: 'long', 
    day: 'numeric' 
  };
  return new Date(dateString).toLocaleDateString('id-ID', options);
}

// Fungsi untuk format waktu
export function formatTime(dateString) {
  const options = { 
    hour: '2-digit', 
    minute: '2-digit',
    hour12: false
  };
  return new Date(dateString).toLocaleTimeString('id-ID', options) + ' WIB';
}

// Fungsi untuk format harga
export function formatPrice(price) {
  if (price === 0) return 'Gratis';
  return new Intl.NumberFormat('id-ID', {
    style: 'currency',
    currency: 'IDR',
    minimumFractionDigits: 0
  }).format(price);
}
